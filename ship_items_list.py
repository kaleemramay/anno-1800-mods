def modparser(guid, name):
    textparsed = """
    <!-- {name} -->
    <ModOp Type=\"add\" GUID=\'{guid}\' Path=\"/Values\">
        <ItemEffect>
            <EffectTargets>
                <Item>
                    <GUID>191458</GUID>
                </Item>
            </EffectTargets>
        </ItemEffect>
    </ModOp>      
    """.format(name=name, guid=guid)
    return textparsed


